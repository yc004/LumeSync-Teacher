const fs = require('fs');
const path = require('path');
const vm = require('vm');
const { loadCoreModule } = require('./core-paths');
const renderEngine = loadCoreModule('render-engine');

const repoRoot = path.join(__dirname, '../..');
const defaultPublicRoot = path.join(repoRoot, 'shared/public');
const teacherShellSrcDir = path.join(repoRoot, 'shared/teacher-shell/src');

const TEACHER_SHELL_SOURCES = [
    path.join(teacherShellSrcDir, 'components/window-controls.tsx'),
    path.join(teacherShellSrcDir, 'components/log-viewer.tsx'),
    path.join(teacherShellSrcDir, 'panels/settings-panel.tsx'),
    path.join(teacherShellSrcDir, 'views/classroom-view.tsx'),
    path.join(teacherShellSrcDir, 'views/course-selector.tsx'),
    path.join(teacherShellSrcDir, 'views/student-waiting-room.tsx'),
    path.join(teacherShellSrcDir, 'views/submissions-browser.tsx'),
    path.join(teacherShellSrcDir, 'app.tsx'),
];

const TEACHER_RENDER_ENGINE_SOURCES = renderEngine.getTeacherRenderEngineSources({ preferSource: true });

function loadBabel(publicRoot = defaultPublicRoot) {
    const babelPath = path.join(publicRoot, 'lib/babel.min.js');
    const source = fs.readFileSync(babelPath, 'utf8');
    const context = {
        console,
        process,
        setTimeout,
        clearTimeout,
    };
    context.window = context;
    context.self = context;
    vm.createContext(context);
    vm.runInContext(source, context, { filename: babelPath });

    if (!context.Babel || typeof context.Babel.transform !== 'function') {
        throw new Error(`Babel standalone did not initialize from ${babelPath}`);
    }
    return context.Babel;
}

function getLatestMtimeMs(paths) {
    return Math.max(...paths.map((filePath) => fs.statSync(filePath).mtimeMs));
}

function normalizeSourceForBundle(source) {
    return source
        .replace(/^(?:const|let|var)\s+React\s*=\s*window\.React\s*;\s*$/gm, '')
        .replace(/^(?:const|let|var)\s+ReactDOM\s*=\s*window\.ReactDOM\s*;\s*$/gm, '')
        .replace(/^const\s+\{\s*[^}]*use[A-Za-z][^}]*\}\s*=\s*React\s*;\s*$/gm, '')
        .replace(/^const\s+\{\s*createPortal\s*\}\s*=\s*ReactDOM\s*;\s*$/gm, '');
}

function compileBundle({ publicRoot, outputPath, sources, header, skipIfFresh }) {
    const shouldSkipIfFresh = skipIfFresh !== false;

    if (
        shouldSkipIfFresh
        && fs.existsSync(outputPath)
        && fs.statSync(outputPath).mtimeMs >= getLatestMtimeMs(sources)
    ) {
        return { outputPath, skipped: true };
    }

    const Babel = loadBabel(publicRoot);
    const chunks = sources.map((sourcePath) => {
        const source = normalizeSourceForBundle(fs.readFileSync(sourcePath, 'utf8'));
        const compiled = Babel.transform(source, {
            presets: ['react', 'typescript'],
            filename: sourcePath,
            sourceType: 'script',
        }).code;

        return [
            `// ---- ${path.relative(repoRoot, sourcePath).replace(/\\/g, '/')} ----`,
            compiled,
            '',
        ].join('\n');
    });

    fs.writeFileSync(
        outputPath,
        [
            ...header,
            '(() => {',
            '"use strict";',
            'const React = window.React;',
            'const ReactDOM = window.ReactDOM;',
            'const { useState, useEffect, useRef, useMemo, useCallback } = React;',
            'const { createPortal } = ReactDOM;',
            '',
            ...chunks,
            '})();',
        ].join('\n'),
        'utf8'
    );

    return { outputPath, skipped: false };
}

function buildTeacherShell(options = {}) {
    const publicRoot = options.publicRoot || defaultPublicRoot;

    return compileBundle({
        publicRoot,
        outputPath: options.outputPath || path.join(publicRoot, 'teacher-app.js'),
        sources: options.sources || TEACHER_SHELL_SOURCES,
        skipIfFresh: options.skipIfFresh,
        header: [
            '// This file is generated by server/src/teacher-shell-build.js.',
            '// Edit shared/teacher-shell/src/* instead.',
        ],
    });
}

function buildTeacherRenderEngine(options = {}) {
    const publicRoot = options.publicRoot || defaultPublicRoot;

    return compileBundle({
        publicRoot,
        outputPath: options.outputPath || path.join(publicRoot, 'render-engine.js'),
        sources: options.sources || TEACHER_RENDER_ENGINE_SOURCES,
        skipIfFresh: options.skipIfFresh,
        header: [
            '// This file is generated by server/src/teacher-shell-build.js.',
            '// Edit core/src/browser/engine/* instead.',
        ],
    });
}

function buildTeacherBundles(options = {}) {
    return [
        buildTeacherShell(options),
        buildTeacherRenderEngine(options),
    ];
}

if (require.main === module) {
    for (const result of buildTeacherBundles({ skipIfFresh: false })) {
        console.log(`[teacher-shell-build] ${result.skipped ? 'skipped' : 'wrote'} ${result.outputPath}`);
    }
}

module.exports = {
    buildTeacherShell,
    buildTeacherRenderEngine,
    buildTeacherBundles,
};
